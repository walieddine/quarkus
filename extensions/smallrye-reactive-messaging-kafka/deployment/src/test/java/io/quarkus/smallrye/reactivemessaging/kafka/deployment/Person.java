package io.quarkus.smallrye.reactivemessaging.kafka.deployment;

public class Person {
    String name;
    int age;

    public Person() {
        // For mappers.
    }

    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public Person setName(String name) {
        this.name = name;
        return this;
    }

    public int getAge() {
        return age;
    }

    public Person setAge(int age) {
        this.age = age;
        return this;
    }
}
